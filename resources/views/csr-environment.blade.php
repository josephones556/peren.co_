                <h2 class="title-normal">Agroforestry Project in the DRC</h2>
      <p><span>In the DRC, Perenco has committed until 2020 to an agroforestry project on a surface of 900 hectares located on the Yema plateau in Muanda territory. This project of sustainable development aims to help the local community reach self-sufficiency in terms of fruit and vegetable production while using tree planting to counteract climate change.</span></p>
    
