                <h2 id="converting-offshores-platforms-into-artificial-reefs" class="question ">Converting Offshores Platforms into Artificial Reefs</h2>
              <div class="ans-box"><p>In Gabon, Perenco and the National Parks Agency (ANPN) have signed a Memorandum of Understanding to conduct research work on <strong>the protection of marine ecosystems</strong> around oil platforms. This protocol is based on results from environmental studies carried out jointly by the ANPN, the National Geographic Society, the Waitt Institute and the Wildlife Conservation Society, which tend to prove that offshore platforms - being areas preserved from fishing- constitute an environment conducive of marine biodiversity development.</p>
    
    <p>These conclusions allow us to envisage a second life as artificial reefs for platforms that will be deactivated. As an operator of mature fields Perenco is particularly invested in the works deployed at the heart of the Mayumba National Park, close to the fields operated in Southern Gabon; an area where large populations of cetaceans and sea turtles have been observed.</p>
