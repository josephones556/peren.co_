    <div class="question-box  ">
                <h2 id="towards-a-new-business-model" class="question ">Towards a new business model</h2>
              <div class="ans-box"><p>Perenco is currently the premier oil and gas producer in Cameroon where it has operated since 1993. To combat the natural decline in oil production and respond to the growing needs of the country, Perenco, in partnership with the Société Nationale des Hydrocarbures of Cameroon, has chosen to develop the LNG <strong>market</strong>, a real growth relay for the company.</p>
    
    <p>In its liquefied form, natural gas can be transported by boat over great distances, thereby facilitating worldwide trade exchanges. Not so long ago, the production of Cameroonian fields was not sufficient to support the investments needed to develop a ground liquefaction plant. Perenco has therefore developed a complex project that makes the operation of these mature fields profitable with a strong GOR - Gas Oil Ratio.</p>
    
    <p>The conversion of a LNG tanker into <strong>a floating liquefaction and storage unit (FLNG), the optimisation of Sanaga 1 offshore platform and the modification of the Bipaga treatment onshore facilities, are at the heart of this ambitious and innovative project</strong>. By exploiting existing synergies between the Sanaga field and the Bipaga complex, and through pragmatic project management and constant leverage, Perenco has attained a profitability threshold well below that of the market, making it possible to exploit LNG in Cameroon.</p>
    
    <p>The successful start of liquefaction operations in Cameroon will contribute to the emergence of this type of project, opening new development opportunities on marginal offshore fields that have so far not been considered profitable.</p>
