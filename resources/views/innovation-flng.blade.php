                <h2 class="title-normal">A world first: the conversion of a LNG tanker into a FLNG Unit</h2>
      <p>The "Hilli Episeyo", property of the Norwegian Golar, is the <strong>first FLNG resulting from the conversion of a 294 meters LNG tanker</strong> built in 1975. Anchored 2 km from the Sanaga 1 platform off Kribi since 20 November 2017, it has now been equipped with four liquefaction trains, each to produce between 500,000 to 700,000 tons per year of LNG with an on-board storage of 125,000 m3. LNG carriers with a capacity of 70,000 to 175,000 m3 can be stowed and the loading is carried out by three transfer arms at a flow rate of 10,000 m3/hour.</p>
    
    <p>The conversion of an existing asset has made it possible to offer a quick and cost-effective solution. 4,000 people have worked for three years to complete this conversion.</p>
    
      
    
