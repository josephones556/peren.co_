        <p>FLNG Hilli Episeyo is the first floating liquefaction plant in the world resulting from the conversion of a LNG tanker. Set-up off Kribi it demonstrates Perenco’s ability to develop innovative and ambitious, even pioneering projects, and once again confirms the company’s technical and economic know-how to exploit mature fields. It is a project that expands company horizons, accessing the closed circle of LNG producers.</p>
    
