                <h2 class="title-normal">Complex facilities</h2>
      <p>In 2017, <strong>four new wells</strong> were drilled on the Sanaga platform in addition to the two existing ones, so as to be able to supply 200 million cubic feet of gas per day.</p>
    
    <p>Sanaga South <strong>gas fields,</strong> the Bipaga complex and the FLNG "Hilli Episeyo" have been connected by 56 kilometres of multiphase pipeline to transport gas to the onshore processing plant (CPF) to be separated from its liquids by low temperature treatment, then exported to the floating terminal by three turbochargers to be liquefied and stored.</p>
    
      
    
          
      </div>
    
    </div></div>
                  <div>
    <div class="question-box  ">
      <p>The liquefied petroleum gas "LPG" is extracted by a series of two columns and stored in two spheres to supply the local market. The condensates are stabilised and sent to tanker "La LOBE".</p>
    
    <p>The extension works of Bipaga gas treatment facilities were carried out over the past two years and have increased available treatment capacity from 40 million cubic feet to a maximum of 250 million cubic feet per day. These works have a sizeable scope both internationally and locally. There are no less than n 37 processing modules produced all over the world that have then been installed by local companies. 700 people were involved in carrying out the work, which will have lasted less than 24 months thanks to a fully committed, professional and motivated projects team.</p>
    
    <p>These facilities as a whole will allow Perenco and the SNH to produce 1,200,000 tons of Liquefied Natural Gas (LNG) for exporting, 26,000 tons of domestic gas for Cameroon households, thereby reducing the exports of the country by half; and 3,300 BOPD stabilised condensates exported on the Ebome field, giving a second life to tanker “La Lobe".</p>
    
    <p>This large scale project signifies a turning point for Perenco with the production and marketing of LNG, a high demand energy source positioned as the most abundant fossil fuel energy and the least harmful from the viewpoint of greenhouse gas emissions.</p>
    
    <p>The FLNG Hilli Episeyo, which is totally displaceable, offers new opportunities that are fully aligned with the Perenco growth strategy: growth without technical or geographical limit and by highlighting the ways our know-how makes a difference.</p>
    
