    <div class="question-box  ">
      <p>Perenco also operates gas projects. Sanaga was the first offshore gas field to be developed in Cameroon to power the Kribi power station and to meet the growing demand for electricity in the country. The production of the two offshore wells is then exported to a processing plant, where the gas is processed to the required standard of 216 MW.</p>
    
    <p>On 30 September 2015, Perenco and the SNH signed a ten-year gas agreement with the Norwegian Golar LNG, with the aim to develop Sanaga's remaining LPG reserves for the domestic market and LNG for export. Such a development could become a reality thanks to FLNG Hilli Episeyo, the first floating liquefaction plant, resulting from the conversion of an LNG carrier. This should allow Perenco and the SNH to produce 1,200,000 tons of Liquefied Natural Gas (LNG) per year for exports; and 26,000 tons of domestic gas for local households, t<span><span>hus reducing the country’s <span>imports</span> by half</span></span> ; with 3,300 BOPD of stabilised condensates exported to the Ebome field, thus giving a second life to tanker La Lobe.</p>
    
