                <h2 class="title-normal">A long-term optimisation</h2>
      <p>The EOV platform is part of an already-operational infrastructure for the Northern fields; namely the FSO Fernan Vaz to secure the storage of its production, the Batanga electrical plant to power its facilities and the pipeline network extended by 1 km.</p>
    
    <p>Since production started, the first two wells have provided 4,000 barrels/day. Drilling teams are already working on reconnecting the 3rd well, while Geosciences teams are studying future projects. 6 million barrels of reserves are expected for 20 million barrels in place.</p>
    
