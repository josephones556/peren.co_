                <h2 id="an-unprecedented-lifting-operation" class="question ">An unprecedented lifting operation</h2>
              <div class="ans-box"><p>At the end of the study and following a complex well recovery (given the age of the drilling facilities and their H2S content) the culmination of this project was the lifting of the future platform 10 km away from the Gabonese coasts. An unprecedented operation that consisted of using Ogma, a former drilling rig, as a lifting platform, rather than chartering a service boat at an exorbitant cost.</p>
    
    <p>After numerous tests to guarantee the feasibility of the operation and its safety, engineers applied their ingenuity to successfully complete this performance, adapt the tool to the platform and the platform to the tool, and cope with many unforeseen events.</p>
    
    <p>Another special feature of the project lies in the routing of the platform elements, i.e. the jacket (250 tons), an intermediate part (35 tons) and the top side (230 tons) to the footstep of Ogma on Barge B57. Barge B57 had transported the structure from the United States to Port-Gentil a few months before. Lifting phases and installing all three elements with the help of divers, welders and all of the technicians took place within the time-limits set, despite unexpected events and uncertain weather.<br />
    This technical performance is truly representative of Perenco’s know-how: moving away from standard methodologies, working in perfect synergy between the different operational teams, and showing ingenuity and responsiveness.</p>
