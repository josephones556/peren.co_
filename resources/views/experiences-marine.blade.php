              <div class="ans-box"><p>One of the main missions of the Marine department is to supervise oil terminals integrity management and provide technical support to subsidiaries.</p>
    
    <p>Perenco has the largest fleet in the region with six floating units in West Africa.</p>
    
    <p>The fleet is composed of Floating Storage Units (FSO) and floating production, storage and offloading units (FPSO). These tank ships are particularly effective in areas located far off the coast and offer an economically attractive solution to collect the oil produced on platforms and store it; and for FPSO, treat it by separating effluents (water, gas and crude). These terminals also serve as an export point for the delivery of crude to our customers</p>
