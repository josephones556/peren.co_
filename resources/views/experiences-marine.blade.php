                <h2 id="designing-and-heading-fleet-improvement-and-extension-projects" class="question ">Designing and heading fleet improvement and extension projects</h2>
              <div class="ans-box"><p>A second component of our activity is to develop projects aimed at improving the operability of our fleet. For example we can equip a floating terminal with a remote boost pump set on a mooring buoy to minimise risks and provide greater comfort to our customers during removal phases. It is a real value creation that can increase the final price of the product.</p>
    
    <p>We also manage the acquisition of new facilities and the renewal of our fleet. We regularly need to replace an aging floating unit. In this case our role is to identify a boat on the market, purchase it and shape its transformation according to our needs. The future terminal must be adapted to our work and to the climatic conditions of its new destination while answering our storage and processing requirements. Following conversion, the last steps are routing until the production site and connection. Such a large scale project can last about two years. Our latest acquisition is representative of the way we operate.</p>
