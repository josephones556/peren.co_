      <p>Today, the spirit of Hubert Perrodo, the founder of Perenco, is still alive in the company.&nbsp;<br />
    Hubert was the son of a fisherman, with a strong entrepreneurial spirit. He created a rental company for service barges in 1975 before becoming a ship-owner. In 1981 he founded Techfor, a drilling company that he resold in 1992 to focus on the acquisition of oil and gas interests, thereby giving rise to Perenco. From then onwards the company quickly specialised in the operation of mature oilfields, a strategy that brought worldwide success.</p>
    
    <p>In 2006 when Hubert Perrodo passed away following a tragic accident, his elder son François took control of the company, determined to continue the adventure. In 2016 he gave a new course to the company and changed the management team with a view to balance growth and profitability while respecting people and the environment. While capitalising on the founding values of the company and on Perenco’s technical specificity, François has also chosen to deploy an integration strategy to include related services such as drilling, and to implement a diversification strategy, especially through the exploitation of gas.</p>
    
    <p>As a legacy Hubert Perrodo has left the desire along with the will of surpassing oneself and a natural instinct for doing things differently. Thanks to these values Perenco looks to the future positively and is ready to face tomorrow's challenges; continue to develop a unique know-how while diversifying towards new markets and guarantee the independence and the sustainability of the company over time.</p>
    
