    <div class="question-box  ">
                <h2 id="multidisciplinary-teams" class="question ">Multidisciplinary teams</h2>
              <div class="ans-box"><p>Geosciences bring together several professions to try to understand what is happening underground, what has happened and what will happen, and to position future drainage and injection points, estimate reserves and identify the work to be carried out on wells.<br />
    <br />
    <strong>Geophysicists</strong> try to reconstitute the subsoil image by means of seismic prospection to model and characterise reservoirs. <strong>Geologists observe</strong> and make assumptions to explain the rock deposition history and deformations in order to understand the current layout. <strong>Reservoir engineers</strong> study the dynamics of the different fluids flow within a producing field to determine how to best operate it.<br />
    <br />
    Alongside them, two other professionals help optimising Geoscience work. <strong>Petrophysicists</strong> gather and analyse the physical measurements collected in wells; and Petroleum Engineers work at well level to optimise <strong>their production.</strong><br />
    <br />
    Together they put forward development plans to the drilling and work-over teams. Finally a headquarters team fully dedicated to <strong>technical services manages</strong> specialised data and software to support geosciences and help them work most effectively.</p>
