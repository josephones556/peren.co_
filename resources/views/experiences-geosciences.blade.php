        <p>Geosciences are by essence multidisciplinary, from data collection and analysis to monitoring drilling campaigns, while the multicultural environment at Perenco offers a considerable range of projects in a constantly moving, human-size company. Studying a reservoir is a team work that brings together geophysicists, geologists and reservoir engineers among other professionals.</p>
    
