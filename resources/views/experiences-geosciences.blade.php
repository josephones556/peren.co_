                <h2 class="title-normal">Ever-changing professions and internal expertise</h2>
      <p>Our various professions keep evolving as a result of technological progress and in particular thanks to the improvement in computer calculating capabilities.</p>
    
    <p>This is particularly the case in geophysics. Today the power of mathematical processing makes it possible to add-value on old data previously acquired through regular reprocessing carried-out every three to five years, which permits to obtain new, clearer and finer images.</p>
    
    <p>This also requires regular new seismic acquisitions to obtain even better quality data, which will in turn be reprocessed several times in the following years.</p>
    
      
    
