    <div class="question-box  ">
      <p><span>It owns and operates the largest infrastructure on the UKCS, comprising 45 offshore platforms, 14 subsea wells, and a network of more than 2,400 km of pipelines connected to its two onshore terminals at Bacton and Dimlington where the gas is received, treated, metered and then exported into the UK National Grid.</span></p>
    
    <p><span>We are responsible for over 10% of the entire UKCS well stock and have more than 200 wells permanently producing wells across 40 gas fields; Leman, Indefatigable, LAPS, Trent, Cleeton including Wollaston, Whittle, Ravenspurn North and Ravenspurn South, West Sole comprising West Sole, Hyde, Hoton and Newsham fields and the Amethyst field. West Sole recently celebrated 50 years of continuous production and still produces 6300 boepd.</span></p>
    
    <p><span>Perenco has been actively investing in its assets since taking over. In 2006, we designed, built and installed the first unmanned compression hub in the North Sea to increase reserves from Trent, Tyne and Tors fields; this is still in service as of today.In 2017, Perenco drilled and tested a new exploration well, with Bp in partnership, on the Carboniferous tight gas target, which lies several hundred of metres below the existing Ravenspurn North gas field. Two further wells on the existing accumulation are planned in 2019.</span></p>
    
    <p><span>Perenco is committed to continue its development strategy of extending the field life and boosting production. A project called RaDiCle has been implemented to remove offshore compression at Ravenspurn and Cleeton and utilise more reliable onshore compression at the Dimlington terminal. Lower offshore operating pressure has enabled the wells to flow more and offshore costs have been reduced</span>.</p>
    
    <p><span>In the North Sea, Perenco must continually face the new challenge of managing highly mature fields which are approaching the end of their economic life. Maintaining the integrity of often more than 40 years old offshore platforms in the very harsh North Sea conditions is a challenge and Perenco has been innovative in optimising its logistics by utilising W2W (walk to work vessels) to deploy workers which has reduced the need for helicopters and increased work efficiency. A lower cost base means the field life can be extended. Perenco, is a mature field specialist, but when the economic life of the field is reached production will cease and decommissioning must start once the necessary regulatory approvals are granted. Five decommissioning operations are being conducted, two are at the final stages - Welland (2010-2016), Thames (2013-2018). The removal of the Tyne (started in 2016) and Guinevere Platforms will take place in 2019. An ongoing campaign to abandon Pickerill wells and isolating surface equipment is currently taking place and will continue for most of 2019.</span></p>
    
