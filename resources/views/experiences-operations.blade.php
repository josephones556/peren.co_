                <h2 id="optimisation-at-the-core-of-our-action" class="question ">Optimisation at the core of our action</h2>
              <div class="ans-box"><p>Our main mission is to optimise well production. Activation types, by gas lift injection or ground pump are determined according to well parameters - in particular the existing pressure and effluent composition. Our production is monitored by rotating teams who check well parameters and behaviour. Monitoring is carried out on the field and in monitoring rooms installed in subsidiaries and at headquarters.</p>
    
    <p>Our second role is to manage<strong> the process</strong> by separating production fluids into their oil, gas and water components. Finally <strong>the last step is to route our production to a storage point</strong> on one of our offshore stockers or to a ground terminal.</p>
    
    <p>Our teams also manage all the logistics between subsidiaries and sites, ensure all operations are compliant with HSE procedures and maintain our facilities.</p>
