              <div class="ans-box"><p>Operations bring together a multitude of professional bodies and multicultural teams.</p>
    
    <p>On site we mainly meet with mechanics, electricians, automation engineers, HSE managers and production engineers who are responsible for the overall understanding of processes and wells. Our teams must demonstrate their adaptability to work on projects with flexible scopes. They must show initiative and have the ability to question operating procedures. They must also have strong relational qualities in order to work with other Perenco professionals in countries which all have their own cultural specificities.</p>
    
    <p>If storage is carried out on a floating unit, the existing team is supplemented by technicians and engineers from the Marine department.</p>
    
