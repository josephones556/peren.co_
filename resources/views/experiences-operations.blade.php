    <p>At Perenco we do not give orders, we operate. As a consequence we are committed not to outsource what is the core of our profession. However our service internalisation policy is not always systematic. It is designed according to our needs and depending on the areas where we operate.</p>
    
    <p>If a service is required in the long term we usually prefer incorporating it, for example well measurement (logging), perforating or work-over. At Perenco the internalisation of these skills is experienced as a strength that allows us to leverage service costs and service quality.</p>
    
      
    
