                <h2 id="a-real-challenge-for-the-industry" class="question ">A real challenge for the industry</h2>
              <div class="ans-box"><p>When we deal with a decommissioning project we discover a very old platform, often built by a third party operator with hardly any documented standards that can date back to the 1960s’. The first step is to analyse the entire installation. This audit phase allows us to define specifications and validate operation costs.</p>
    
    <p>This goes hand in hand with an analysis of local regulations and an environmental impact study to define the final perimeter of the works to be undertaken with the authorities. Apart from finding alternative ideas to re-use the facilities, decommissioning can at times be more destructive for the environment that already in-situ solution, so we must study each specific case individually.</p>
    
    <p>We start the operational phase by permanently cementing the well and completely sealing it. Then, we proceed to the cleaning of all facilities that were used in the process and to the removal of hydrocarbons, so that we do not contaminate the environment. This step can last several months. Finally we proceed to the most spectacular stage: the removal of the platform. All installations are transported on land for final treatment either for reuse elsewhere, or for recycling according to the environmental rules of the relevant country.</p>
    
    <p>From the first decommissioning projects, Perenco immediately chose to integrate all the resources, technologies and skills required. In a very new market with hardly any references, the company had to deploy all its know-how in engineering, create new processes, explore administrative procedures - especially regarding permits- and find suitable partners; all while carefully controlling costs. Our oil expertise and agile operating methods have indeed brought real benefits.</p>
