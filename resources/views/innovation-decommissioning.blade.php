    <div class="question-box  ">
                <h2 id="a-technical-and-economic-challenge" class="question ">A technical and economic challenge</h2>
              <div class="ans-box"><p>At Perenco we approach decommissioning as exploitation, with the same rigor and the same ingenuity, to develop the tools most suited to our operations. We do not proceed by reverse engineering. From the beginning we made the choice to disassemble and remove platforms in parts to avoid chartering very expensive boats. Instead of carrying 5,000 tons at a time, we proceeded in two or three loads. Similarly, we do not use rigs to cement the wells. We deploy more flexible and lighter technologies. This is how Perenco innovates.<br />
    <br />
    Such alternative choices make it possible to service projects in accordance with State regulations. For example, the United Kingdom mandates oversight bodies (OGA, BEIS, etc.) to ensure that decommissioning is carried out in a safe, environmentally sound and cost-effective manner.</p>
