      <p>Production has clearly progressed, reaching again <strong>465,000 BOEPD </strong>through a sustained strategy of external growth and development, with <strong>two major projects</strong>: the commissioning of FLNG in Cameroon, which now produces 180 MMSCFD (i.e. 1.2 million tons per year of LNG) of condensate and LPG; and the acquisition of several assets in Gabon, onshore and offshore, offering a real synergy with Perenco’s historical assets.<br />
    The first notable result of this strategy relates to Perenco's certified reserves, which at the end of 2017 reached<strong> a record level with an exceptional renewal rate of 135%</strong> during the year (169 MMBOE of reserves created for 69 MMBOE of reserves produced).</p>
    
    <p>It is worth noting that the 2017 production was completely renewed thanks to organic growth and doubled by external growth.</p>
    
    <p>This marked improvement now has to be confirmed and sustained thanks to our<strong> technical and economic know-how</strong>, which has a direct impact on our cost-efficiency; our ability to integrate expertise and to develop our assets; without omitting safety and integrity, at the heart of our commitment, as well as maintenance, a true source of performance in our operations.</p>
    
    <p>Perenco’s know-how has allowed us to overcome the crisis and to approach recovery by limiting the increase of our operating costs. This know-how is supported by <strong>a true commitment from our teams and local management</strong>, all willing to take responsibility. They are the best equipped to safeguard our people’s and facilities integrity.&nbsp;I believe it is essential for every collaborator to know they can come to work full of trust, in a peaceful and constructive environment.</p>
    
    <p>Another major development relates to the<strong> consolidation of our activities</strong> in the different countries where we operate such as in Wytch Farm in the United Kingdom where we now own 95% of the assets. Today, thanks to this policy supported by new projects such as Tiko (Cameroon), East Orovinyare (Gabon), Ganga (Gabon) or DRC on the offshore, Perenco has reached <strong>a net/gross ratio of 60%</strong>. This willingness for consolidation fully falls under the economic model of the company: buying marginal production, rationalising, transforming, developing, consolidating and stabilising. A model that also applies to our exploration projects for new fields, for which we favour the most relevant projects, thereby enabling us to assess our know-how and our capacity to innovate on a daily basis.</p>
    
    <p>It is the sustainability of our model that allows us to make a positive contribution in our partner countries, where we are a reassuring presence and constant listeners. <strong>The <a href="innovation/flng">FLNG</a> project</strong> perfectly illustrates this. It is an extensive project with a major local impact for Cameroon through the production of 30,000 tons of domestic gas, thereby reducing the national imports bill by 40%.</p>
    
    <p>Finally, along with the management team we are committed to develop a <strong>participatory and open management </strong>to allow everyone to express themselves and feel fulfilled within our family business. For I do not forget that it is thanks to the technical excellence, the taste for innovation and the spirit of shared responsibility demonstrated by Perenco’s men and women that we have the capacity to meet tomorrow’s challenges. "</p>
    
