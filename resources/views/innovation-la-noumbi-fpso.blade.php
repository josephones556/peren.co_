                <h2 id="making-the-choice-of-a-conversion" class="question ">Making the choice of a conversion</h2>
              <div class="ans-box"><p>When the Production Sharing Contract (CPP) in Congo was renewed, Perenco committed to install a new floating terminal on the Yombo field to replace the Conkouati, a facility acquired in 2002 and in place since 1992.<br />
    <br />
    At the launch of the project at the end of 2013, Perenco first contemplated the construction of a new floating production storage and offloading unit. But following the crisis that affected the entire sector, the group re-oriented the project on a much more rational conversion in terms of costs and planning.<br />
    <br />
    Perenco subsequently acquired an icebreaker petroleum vessel in excellent state, the M/T Tempera, former property of a Finnish oil company. This vessel had the perfect dimensions to suit the needs of the company, a reinforced hull and a diesel-electric propulsion unique in its kind for an oil ship.</p>
