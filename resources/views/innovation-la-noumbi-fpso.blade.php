                <h2 class="title-normal">Multiple benefits</h2>
      <p>The FPSO offers many competitive advantages. Maintenance costs for the old building had become disproportionate when compared to expected profits, while process facilities were no longer in line with the needs of the Congolese subsidiary. Yombo generates more and more water so it was crucial to review fluid separation capacities and cope with future water increase. The new FPSO will also allow Perenco to start producing on the adjacent Masseko field.</p>
    
    <p>Last but not least, new living quarters will offer a better working environment and significantly increase accommodation capacity, thereby reducing living barges charter costs and special operations costs (development, works, maintenance and drilling etc.). The 90 staff members working on site who currently operate the field are to settle in their new quarters from November 2018.</p>
    
      
    
          
