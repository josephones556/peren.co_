                <h2 id="innovative-and-bold-technical-choices" class="question ">Innovative and bold technical choices</h2>
              <div class="ans-box"><p>For this conversion, given the expected volumes to be processed, Perenco chose to use two existing storage tanks on the ship to improve fluid separation efficiency and to considerably reduce the quantity of process equipment to be installed on the deck. This new technical solution required numerous studies to validate fluid diffusion, fluid behaviour and also to define fluid regulation and transfer. The new facilities will provide large savings while offering an effective and scalable system.</p>
