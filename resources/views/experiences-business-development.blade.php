              <div class="ans-box"><p>Our role is also to advise and accompany our 15 subsidiaries' general managers on an individual basis to protect and extend our oil contracts. We operate during negotiation phases whenever our concessions are due to expire but also throughout the duration of contracts. Contracts generally have 20 to 30 years term with legal and economic stability clauses however the legislator may occasionally be tempted to renegotiate certain terms or even modify Oil or Gas Codes (changing the law). Amendment requests may also come from our initiative to allow for the sanction of a particular investment project in order to accelerate organic growth.</p>
    
    <p>Our department intervenes on all major company projects as well, such as the FLNG project, result of a partnership between Perenco and the State of Cameroon for the production, processing and liquefaction of natural gas reserves. This had not been considered technically or economically viable in the past, but contractual arrangements and the "risk matrix" negotiation between all different projects components (upstream/ midstream/downstream) were key in taking the decision to invest. Perenco chose to invest at the end of 2015 while our industry was suffering a difficult downturn. However the quality of our cooperation with the subsidiary and our contractual boldness have made all the difference.</p>
    
    <p>In short, we constantly interact between parties and we are here to support our subsidiaries! Our role thereby consists in facilitating and optimising exchanges and supporting negotiations and commercial relations with our partners.</p>
